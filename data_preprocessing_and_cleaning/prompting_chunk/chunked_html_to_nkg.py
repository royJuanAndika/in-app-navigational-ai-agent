def infer_page_url_from_filename(filename: str) -> str:
    stem = Path(filename).stem
    parts = [p for p in stem.split("_") if p]
    return "/" + "/".join(parts) if parts else "/unknown"


def extract_inline_scripts(html: str) -> str:
    """Extract inline <script> content (no src attr) from HTML for trigger context."""
    scripts = re.findall(r"<script(?:\s[^>]*)?>(.+?)</script>", html, re.DOTALL | re.IGNORECASE)
    # Filter: only keep scripts that are NOT just empty or whitespace
    inline = [s.strip() for s in scripts if s.strip() and not re.match(r"^\s*$", s)]
    if not inline:
        return ""
    return "<script>\n" + "\n</script>\n<script>\n".join(inline) + "\n</script>"


def extract_page_metadata_from_html(html: str) -> tuple[str, str]:
    """Extract page_url + page_title from btn_favorite attributes when available."""
    tag_match = re.search(r"<[^>]*\bid\s*=\s*['\"]btn_favorite['\"][^>]*>", html, flags=re.IGNORECASE)
    if not tag_match:
        return "", ""

    tag = tag_match.group(0)

    def _attr(name: str) -> str:
        m = re.search(rf"\b{name}\s*=\s*(['\"])(.*?)\1", tag, flags=re.IGNORECASE | re.DOTALL)
        return m.group(2).strip() if m else ""

    raw_url = _attr("menu_url")
    page_title = _attr("menu_name")

    page_url = ""
    if raw_url:
        m = re.search(r"(/customer/[^\s\"'#?]+)", raw_url)
        page_url = m.group(1) if m else raw_url

    return page_url, page_title


def infer_page_slug(page_url: str, fallback_filename: str) -> str:
    slug = page_url.rstrip("/").split("/")[-1]
    if slug:
        return slug
    return Path(fallback_filename).stem


def extract_title(html: str) -> str:
    m = re.search(r"<title[^>]*>(.*?)</title>", html, flags=re.IGNORECASE | re.DOTALL)
    if not m:
        return "Untitled"
    title = re.sub(r"\s+", " ", m.group(1)).strip()
    return title or "Untitled"


def parse_dom_ids_with_lines(html: str) -> dict[str, int]:
    id_line: dict[str, int] = {}
    pattern = re.compile(r"\bid\s*=\s*([\"'])([^\"']+)\1", flags=re.IGNORECASE)
    for m in pattern.finditer(html):
        dom_id = m.group(2).strip()
        if not dom_id:
            continue
        if dom_id not in id_line:
            line_no = html.count("\n", 0, m.start()) + 1
            id_line[dom_id] = line_no
    return id_line


def parse_dom_ids(html: str) -> set[str]:
    return set(parse_dom_ids_with_lines(html).keys())


def chunk_html(
    html: str,
    max_chars: int = 18000,
    overlap_chars: int = 1200,
) -> list[Chunk]:
    """
    Split HTML into roughly equal-sized chunks that:

    1. Never exceed `max_chars` (hard ceiling).
    2. Always snap cuts to a clean tag boundary so the LLM never receives
       a truncated opening/closing tag mid-token.
    3. Merge trailing micro-chunks (< 15 % of max_chars and 0 DOM ids) into
       the previous chunk so we never waste an LLM call on whitespace/noise.
    4. Add a context overlap at the start of every non-first chunk so the LLM
       has structural context without duplicating work.
    5. Aim for even chunk sizes by targeting `max_chars * 0.85` as the
       *preferred* cut point, then snapping forward to the next boundary — this
       avoids the old behaviour where boundary-snapping could produce one 2 kchar
       chunk followed by one 17 kchar chunk.
    """
    lines = html.splitlines(keepends=True)
    if not lines:
        return [Chunk(index=1, start_line=1, end_line=1, char_count=0, text="")]

    # Closing tags are preferred cut points because they represent completed
    # structural units.  Opening tags are fallback cuts.
    closing_boundary_re = re.compile(
        r"</(?:div|section|form|table|ul|nav|header|footer|main|article|aside|script)>",
        flags=re.IGNORECASE,
    )
    opening_boundary_re = re.compile(
        r"<(?:div|section|form|table|ul|nav|header|footer|main|article|aside|script)\b",
        flags=re.IGNORECASE,
    )
    id_attr_re = re.compile(r'\bid\s*=\s*["\'][^"\']+["\']', flags=re.IGNORECASE)

    # ------------------------------------------------------------------ #
    # Pre-compute cumulative char offsets so boundary search is O(1).     #
    # ------------------------------------------------------------------ #
    cum: list[int] = [0] * (len(lines) + 1)
    for i, ln in enumerate(lines):
        cum[i + 1] = cum[i] + len(ln)

    def chars_in_range(a: int, b: int) -> int:
        """Char count for lines[a:b] (0-indexed)."""
        return cum[b] - cum[a]

    def dom_ids_in_range(a: int, b: int) -> int:
        return sum(1 for ln in lines[a:b] if id_attr_re.search(ln))

    # ------------------------------------------------------------------ #
    # Find the best snap point starting at `search_from` (inclusive),     #
    # scanning forward up to `search_to` (exclusive).                     #
    # Returns the line index *after* the cut (i.e. lines[:cut] is kept).  #
    # Prefers closing tags; falls back to opening tags; falls back to      #
    # `search_to` itself if no boundary is found.                          #
    # ------------------------------------------------------------------ #
    def best_snap(search_from: int, search_to: int) -> int:
        best_close: int | None = None
        best_open: int | None = None
        for i in range(search_from, search_to):
            line = lines[i]
            if closing_boundary_re.search(line):
                best_close = i + 1   # cut *after* this line
            elif opening_boundary_re.search(line) and best_open is None:
                best_open = i        # cut *before* this opening tag
        if best_close is not None:
            return best_close
        if best_open is not None:
            return best_open
        return search_to

    # ------------------------------------------------------------------ #
    # Main chunking loop                                                   #
    # ------------------------------------------------------------------ #
    # We target `preferred_chars` as the soft size and use `max_chars` as
    # the hard ceiling.  Targeting 85 % of max keeps us well below the
    # ceiling after boundary snapping (which can only add lines, not remove
    # them) and produces chunks that are visually similar in size.
    preferred_chars = int(max_chars * 0.85)

    raw_chunks: list[tuple[int, int]] = []   # (start_line_0idx, end_line_0idx exclusive)
    start = 0

    while start < len(lines):
        total_remaining = chars_in_range(start, len(lines))

        # If whatever is left fits in one chunk, take it all.
        if total_remaining <= max_chars:
            raw_chunks.append((start, len(lines)))
            break

        # Walk forward to find where we hit the preferred target.
        preferred_end = start
        acc = 0
        while preferred_end < len(lines):
            acc += len(lines[preferred_end])
            preferred_end += 1
            if acc >= preferred_chars:
                break

        # Search for a snap point in the window [preferred_end, hard_limit].
        # This ensures we never go below preferred and never exceed hard.
        hard_end = start
        acc2 = 0
        while hard_end < len(lines):
            acc2 += len(lines[hard_end])
            hard_end += 1
            if acc2 >= max_chars:
                break

        snap = best_snap(preferred_end, hard_end)
        # Guard: snap must always advance past start by at least one line.
        snap = max(snap, start + 1)
        # Guard: never exceed hard ceiling.
        if chars_in_range(start, snap) > max_chars:
            snap = hard_end

        raw_chunks.append((start, snap))

        # Compute overlap: rewind from `snap` to include `overlap_chars` of
        # context.  This gives the next chunk structural awareness without
        # duplicating a full chunk's worth of content.
        back = snap
        overlap_acc = 0
        while back > start and overlap_acc < overlap_chars:
            back -= 1
            overlap_acc += len(lines[back])

        next_start = back if back > start else snap
        # Safety: always advance.
        if next_start <= start:
            next_start = snap
        start = next_start

    # ------------------------------------------------------------------ #
    # Post-process: merge micro tail-chunks into the previous chunk.      #
    # A chunk is a "micro tail" when it is the last chunk AND             #
    # its char count < 15 % of max_chars AND it contains 0 DOM ids.      #
    # ------------------------------------------------------------------ #
    micro_threshold = int(max_chars * 0.15)

    while len(raw_chunks) >= 2:
        last_s, last_e = raw_chunks[-1]
        tail_chars = chars_in_range(last_s, last_e)
        tail_ids = dom_ids_in_range(last_s, last_e)
        if tail_chars <= micro_threshold and tail_ids == 0:
            # Absorb into the previous chunk (ignoring overlap boundary).
            prev_s, _ = raw_chunks[-2]
            raw_chunks[-2] = (prev_s, last_e)
            raw_chunks.pop()
        else:
            break

    # ------------------------------------------------------------------ #
    # Build Chunk objects                                                  #
    # ------------------------------------------------------------------ #
    chunks: list[Chunk] = []
    for idx, (s, e) in enumerate(raw_chunks, start=1):
        text = "".join(lines[s:e])
        chunks.append(
            Chunk(
                index=idx,
                start_line=s + 1,
                end_line=e,
                char_count=chars_in_range(s, e),
                text=text,
            )
        )

    # Re-index after potential merges.
    for i, ch in enumerate(chunks, start=1):
        ch.index = i

    return chunks


def validate_nkg_fragment(data: dict[str, Any]) -> None:
    if "page" not in data or not isinstance(data["page"], dict):
        raise ValueError("Missing/invalid page")
    if "elements" not in data or not isinstance(data["elements"], list):
        raise ValueError("Missing/invalid elements list")
    if "triggers" not in data or not isinstance(data["triggers"], list):
        raise ValueError("Missing/invalid triggers list")


def normalize_element(el: dict[str, Any], dom_ids: set[str], page_slug: str) -> dict[str, Any]:
    element_id = str(el.get("id", "")).strip()
    desc = str(el.get("desc", "")).strip()
    element_type = str(el.get("type", "section")).strip() or "section"
    selector = str(el.get("selector", "")).strip()
    parent_element_id = str(el.get("parent_element_id", "")).strip()

    if not element_id:
        return {}

    if element_id in dom_ids:
        selector = f"#{element_id}"
    elif not selector:
        selector = f"[data-nkg-id='{element_id}']"

    if "__" not in element_id and element_id not in dom_ids:
        element_id = f"{page_slug}__{element_id}"

    if parent_element_id:
        if "__" not in parent_element_id and parent_element_id not in dom_ids:
            parent_element_id = f"{page_slug}__{parent_element_id}"
        if parent_element_id == element_id:
            parent_element_id = ""

    if not desc:
        desc = "Elemen antarmuka untuk interaksi pengguna"

    normalized = {
        "id": element_id,
        "desc": desc,
        "type": element_type,
        "selector": selector,
    }

    if parent_element_id:
        normalized["parent_element_id"] = parent_element_id

    return normalized


def normalize_trigger(tr: dict[str, Any]) -> dict[str, Any]:
    from_id = str(tr.get("from", "")).strip()
    to = str(tr.get("to", "")).strip()
    to_type = str(tr.get("to_type", "")).strip()

    if not from_id or not to:
        return {}

    if to_type not in {"element", "page"}:
        to_type = "page" if to.startswith("/") else "element"

    return {"from": from_id, "to": to, "to_type": to_type}


def merge_nkg(base: dict[str, Any], fragment: dict[str, Any], dom_ids: set[str], page_slug: str) -> dict[str, Any]:
    page = base.get("page") or fragment.get("page") or {}

    existing_elements: dict[str, dict[str, Any]] = {
        e["id"]: e for e in base.get("elements", []) if isinstance(e, dict) and "id" in e
    }

    for raw_el in fragment.get("elements", []):
        if not isinstance(raw_el, dict):
            continue
        el = normalize_element(raw_el, dom_ids, page_slug)
        if not el:
            continue

        if el["id"] not in existing_elements:
            existing_elements[el["id"]] = el
        else:
            old = existing_elements[el["id"]]
            if len(el.get("desc", "")) > len(old.get("desc", "")):
                old["desc"] = el["desc"]
            if old.get("type") in {"", "section"} and el.get("type"):
                old["type"] = el["type"]
            if el["id"] in dom_ids:
                old["selector"] = f"#{el['id']}"
            elif not old.get("selector") and el.get("selector"):
                old["selector"] = el["selector"]
            if not old.get("parent_element_id") and el.get("parent_element_id"):
                old["parent_element_id"] = el["parent_element_id"]

    final_ids = set(existing_elements.keys())
    for element_id, element in existing_elements.items():
        parent_id = str(element.get("parent_element_id", "")).strip()
        if not parent_id:
            element.pop("parent_element_id", None)
            continue
        if parent_id == element_id or parent_id not in final_ids:
            element.pop("parent_element_id", None)

    trig_set = {
        (t.get("from"), t.get("to"), t.get("to_type"))
        for t in base.get("triggers", [])
        if isinstance(t, dict)
    }
    triggers = [t for t in base.get("triggers", []) if isinstance(t, dict)]

    for raw_tr in fragment.get("triggers", []):
        if not isinstance(raw_tr, dict):
            continue
        tr = normalize_trigger(raw_tr)
        if not tr:
            continue
        key = (tr["from"], tr["to"], tr["to_type"])
        if key not in trig_set:
            trig_set.add(key)
            triggers.append(tr)

    merged = {
        "page": {
            "id": str(page.get("id", "")).strip() or base.get("page", {}).get("id", ""),
            "title": str(page.get("title", "")).strip() or base.get("page", {}).get("title", "Untitled"),
            "desc": str(page.get("desc", "")).strip() or base.get("page", {}).get("desc", "Halaman admin untuk pengelolaan data."),
        },
        "elements": sorted(existing_elements.values(), key=lambda x: x["id"]),
        "triggers": triggers,
    }
    return merged


