def _sanitize_backslashes(s: str) -> str:
    """Replace invalid JSON backslash escapes by scanning char by char."""
    valid_escapes = set('"\\\/bfnrtu')
    result = []
    i = 0
    while i < len(s):
        ch = s[i]
        if ch == '\\':
            nxt = s[i+1] if i+1 < len(s) else ''
            if nxt == 'u':
                # Check if followed by exactly 4 hex digits
                hex_part = s[i+2:i+6]
                if len(hex_part) == 4 and all(c in '0123456789abcdefABCDEF' for c in hex_part):
                    result.append('\\u')
                    i += 2
                    continue
                else:
                    result.append('\\\\u')
                    i += 2
                    continue
            elif nxt in valid_escapes:
                result.append('\\')
                result.append(nxt)
                i += 2
                continue
            else:
                result.append('\\\\')
                i += 1
                continue
        result.append(ch)
        i += 1
    return ''.join(result)

def extract_json_from_text(text: str) -> dict[str, Any]:
    text = text.strip()

    # Strip thinking blocks and markdown fences
    text = re.sub(r"<think>.*?</think>", "", text, flags=re.DOTALL).strip()
    text = re.sub(r"^```(?:json)?\s*", "", text, flags=re.MULTILINE)
    text = re.sub(r"```\s*$", "", text, flags=re.MULTILINE).strip()

    # Sanitize ALL invalid JSON backslash escapes BEFORE any parsing attempt.
    # Valid JSON escapes: \" \\ \/ \b \f \n \r \t \uXXXX
    # Strategy: replace any \ not followed by a valid escape char with \\
    # Do \uXXXX check first, then catch everything else.
    text = _sanitize_backslashes(text)

    # Try direct parse (works if output is clean JSON)
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        pass

    # Find outermost JSON object
    start = text.find("{")
    if start == -1:
        raise ValueError("No JSON object found in LLM response")

    depth = 0
    end = -1
    in_string = False
    escape_next = False
    for i in range(start, len(text)):
        ch = text[i]
        if escape_next:
            escape_next = False
            continue
        if ch == "\\" and in_string:
            escape_next = True
            continue
        if ch == '"':
            in_string = not in_string
            continue
        if in_string:
            continue
        if ch == "{":
            depth += 1
        elif ch == "}":
            depth -= 1
            if depth == 0:
                end = i + 1
                break

    if end != -1:
        try:
            return json.loads(text[start:end])
        except json.JSONDecodeError as exc:
            # Print a wide window around the error position
            bad_pos = exc.pos
            print(f"[DEBUG] error at pos {bad_pos}: {repr(text[start:end][max(0,bad_pos-100):bad_pos+100])}")
            raise ValueError(f"JSON parse failed after sanitization: {exc}") from exc

    # Truncation recovery: LLM ran out of tokens mid-JSON
    partial = text[start:]
    stack = []
    in_string = False
    escape_next = False
    for ch in partial:
        if escape_next:
            escape_next = False
            continue
        if ch == "\\" and in_string:
            escape_next = True
            continue
        if ch == '"':
            in_string = not in_string
            continue
        if in_string:
            continue
        if ch in "{[":
            stack.append(ch)
        elif ch == "}" and stack and stack[-1] == "{":
            stack.pop()
        elif ch == "]" and stack and stack[-1] == "[":
            stack.pop()

    closing = "".join("]" if o == "[" else "}" for o in reversed(stack))
    trimmed = partial.rstrip().rstrip(",").rstrip()
    recovered = trimmed + closing
    try:
        result = json.loads(recovered)
        print("[WARN] LLM output was truncated; recovered partial JSON")
        return result
    except json.JSONDecodeError as exc:
        raise ValueError("Unclosed JSON object in LLM response") from exc

def ollama_chat(
    ollama_url: str,
    model: str,
    system_prompt: str,
    user_prompt: str,
    timeout_seconds: int,
    temperature: float = 0.1,
    num_predict: int = 4096,
    use_json_mode: bool = True,
    stream: bool = False,
    think: bool = False,
    print_stream: bool = False,
    ollama_token: str = "",
) -> str:
    payload = {
        "model": model,
        "messages": [
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_prompt},
        ],
        "stream": stream,
        "think": think,
        "options": {
            "temperature": temperature,
            "num_predict": num_predict,
            "repeat_penalty": 1.1,
        },
    }
    
    # Use JSON mode for models that support it (Ollama 0.4+)
    # This constrains output to valid JSON only, improving reliability
    if use_json_mode:
        payload["format"] = "json"

    headers = {"Content-Type": "application/json"}
    if ollama_token:
        headers["X-API-Token"] = ollama_token

    req = request.Request(
        f"{ollama_url.rstrip('/')}/api/chat",
        method="POST",
        data=json.dumps(payload).encode("utf-8"),
        headers=headers,
    )

    try:
        with request.urlopen(req, timeout=timeout_seconds) as resp:
            if not stream:
                data = json.loads(resp.read().decode("utf-8"))
                return data["message"]["content"]

            parts: list[str] = []
            for raw_line in resp:
                line = raw_line.decode("utf-8", errors="ignore").strip()
                if not line:
                    continue

                chunk = json.loads(line)
                if chunk.get("error"):
                    raise RuntimeError(f"Ollama stream error: {chunk['error']}")

                text = (chunk.get("message") or {}).get("content", "")
                if text:
                    parts.append(text)
                    if print_stream:
                        print(text, end="", flush=True)

            if print_stream:
                print()
            return "".join(parts)
    except error.HTTPError as exc:
        body = exc.read().decode("utf-8", errors="ignore")
        raise RuntimeError(f"HTTP {exc.code} from Ollama: {body[:1000]}") from exc
    except error.URLError as exc:
        raise RuntimeError(f"Failed to connect to Ollama at {ollama_url}: {exc}") from exc


def remote_extract_chunk(
    *,
    server_url: str,
    api_token: str,
    filename: str,
    html_content: str,
    page_url: str,
    page_title: str,
    preferred_model: str,
    timeout_seconds: int,
    use_fallback: bool,
    stream: bool = False,
    show_stream: bool = False,
    show_thinking: bool = False,
) -> dict[str, Any]:
    payload = {
        "filename": filename,
        "skeleton": {
            "page_url": page_url,
            "page_title": page_title,
            "filename": filename,
            "html_content": html_content,
        },
        "preferred_model": preferred_model,
        "use_fallback": use_fallback,
    }

    if stream:
        payload["show_thinking"] = show_thinking

        stream_req = request.Request(
            f"{server_url.rstrip('/')}/extract/stream",
            method="POST",
            data=json.dumps(payload).encode("utf-8"),
            headers={
                "Content-Type": "application/json",
                "x-api-token": api_token,
            },
        )

        try:
            with request.urlopen(stream_req, timeout=timeout_seconds) as resp:
                streamed_chars = 0
                last_report = 0
                saw_final = False

                for raw_line in resp:
                    line = raw_line.decode("utf-8", errors="ignore").strip()
                    if not line:
                        continue

                    event = json.loads(line)
                    event_type = event.get("event")

                    if event_type == "model":
                        attempt = event.get("attempt")
                        total_attempts = event.get("total_attempts")
                        model_name = event.get("model", "")
                        print(f"    remote model attempt {attempt}/{total_attempts}: {model_name}")
                        continue

                    if event_type == "status":
                        print(f"    {event.get('message', '')}")
                        continue

                    if event_type == "token":
                        text = event.get("text", "")
                        if not text:
                            continue
                        if show_stream:
                            print(text, end="", flush=True)
                        else:
                            streamed_chars += len(text)
                            if streamed_chars - last_report >= 2000:
                                print(f"    streaming... {streamed_chars} chars")
                                last_report = streamed_chars
                        continue

                    if event_type == "final":
                        saw_final = True
                        if show_stream:
                            print()

                        if event.get("success"):
                            data = event.get("data")
                            if not isinstance(data, dict):
                                raise RuntimeError("Remote stream returned invalid final data payload")
                            return data

                        error_msg = str(event.get("error", "unknown error"))
                        parse_related_markers = (
                            "No JSON object found",
                            "Unclosed JSON object",
                            "JSON",
                            "Expecting",
                        )
                        if any(marker in error_msg for marker in parse_related_markers):
                            print(f"    stream JSON parse issue ({error_msg}); falling back to non-stream mode")
                            return remote_extract_chunk(
                                server_url=server_url,
                                api_token=api_token,
                                filename=filename,
                                html_content=html_content,
                                page_url=page_url,
                                page_title=page_title,
                                preferred_model=preferred_model,
                                timeout_seconds=timeout_seconds,
                                use_fallback=use_fallback,
                                stream=False,
                                show_stream=False,
                                show_thinking=False,
                            )

                        raise RuntimeError(f"Remote stream extract failed: {error_msg}")

                if not saw_final:
                    raise RuntimeError("Remote stream ended without final event")

        except error.HTTPError as exc:
            if exc.code in {404, 405}:
                print("    remote server has no /extract/stream endpoint; falling back to non-stream mode")
                return remote_extract_chunk(
                    server_url=server_url,
                    api_token=api_token,
                    filename=filename,
                    html_content=html_content,
                    page_url=page_url,
                    page_title=page_title,
                    preferred_model=preferred_model,
                    timeout_seconds=timeout_seconds,
                    use_fallback=use_fallback,
                    stream=False,
                    show_stream=False,
                    show_thinking=False,
                )
            body = exc.read().decode("utf-8", errors="ignore")
            raise RuntimeError(f"HTTP {exc.code} from remote stream endpoint: {body[:1000]}") from exc
        except error.URLError as exc:
            raise RuntimeError(f"Failed to connect to remote server at {server_url}: {exc}") from exc

    req = request.Request(
        f"{server_url.rstrip('/')}/extract",
        method="POST",
        data=json.dumps(payload).encode("utf-8"),
        headers={
            "Content-Type": "application/json",
            "x-api-token": api_token,
        },
    )

    try:
        with request.urlopen(req, timeout=timeout_seconds) as resp:
            body = json.loads(resp.read().decode("utf-8"))
    except error.HTTPError as exc:
        body = exc.read().decode("utf-8", errors="ignore")
        raise RuntimeError(f"HTTP {exc.code} from remote server: {body[:1000]}") from exc
    except error.URLError as exc:
        raise RuntimeError(f"Failed to connect to remote server at {server_url}: {exc}") from exc

    if not body.get("success"):
        raise RuntimeError(f"Remote extract failed: {body.get('error', 'unknown error')}")

    data = body.get("data")
    if not isinstance(data, dict):
        raise RuntimeError("Remote extract returned invalid 'data' payload")
    return data


